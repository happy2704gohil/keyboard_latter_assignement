def w(L,B,H,l,b,h):
    w = []
    x = []
    for i in range(0,len(l)):
        for j in range(0,len(L)):
            k = int(H[j]/h[i])
            lm1 = int(L[j]/l[i])*int(B[j]/b[i])
            lm2 = int(L[j]/b[i])*int(B[j]/l[i])
            lm = max(lm1,lm2)
            x.append(k*lm)
        w.append(x)
    return w
